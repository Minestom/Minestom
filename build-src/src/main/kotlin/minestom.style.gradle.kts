                "AttemptedNegativeZero",
                "BadImport",
                "BadInstanceof",
                "BooleanLiteral",
                "BoxingComparator",
                "ClassInitializationDeadlock",
                "DefaultCharset",
                "EffectivelyPrivate",
                "EqualsGetClass",
                "EmptyBlockTag",
                "FutureReturnValueIgnored",
                "JdkObsolete",
                "LongDoubleConversion",
                "MissingSummary",
                "NarrowingCompoundAssignment",
                "NonApiType",
                "NonCanonicalType",
                "StatementSwitchToExpressionSwitch",
                "StringCaseLocaleUsage",
                "StringSplitter",
                "MissingOverride",
                "ObjectToString",
                "OperatorPrecedence",
                "ShortCircuitBoolean",
                "PatternMatchingInstanceof",
                "UnnamedVariable",
                "UnusedMethod",
                "UnusedVariable",
                "UnnecessaryLambda",
                "UnnecessaryParentheses",
                "UnnecessarilyFullyQualified",
                "NullArgumentForNonNullParameter",
                "NullableConstructor",
                "NullableOnContainingClass",
