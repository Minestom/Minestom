package net.minestom.codegen;

import com.palantir.javapoet.*;
import org.jetbrains.annotations.ApiStatus;

import javax.lang.model.element.Modifier;
import java.util.Comparator;
import java.util.List;
import java.util.stream.Stream;

record BuiltinRegistriesGenerator(Codegen codegen) {
    private static final String REGISTRY_PACKAGE = "net.minestom.server.registry";

    private static void addRegistry(TypeSpec.Builder constants, ClassName registryKeyClass, ClassName registryClass,
                                    ClassName valueClass, String constantName, String key) {
        final ParameterizedTypeName registryType = ParameterizedTypeName.get(registryClass, valueClass);
        constants.addField(FieldSpec.builder(ParameterizedTypeName.get(registryKeyClass, registryType), constantName)
                .addModifiers(Modifier.PUBLIC, Modifier.STATIC, Modifier.FINAL)
                .initializer("$T.unsafeOf($S)", registryKeyClass, key)
                .addJavadoc("The registry key for {@link $T}.\n", valueClass)
                .build());
    }

    void generate(List<Generators.StaticRegistrySpec> staticRegistries,
                  List<Generators.DynamicRegistrySpec> dynamicRegistries) {
        final ClassName registryKeyClass = ClassName.get(REGISTRY_PACKAGE, "RegistryKey");
        final ClassName registryClass = ClassName.get(REGISTRY_PACKAGE, "Registry");
        final ClassName generatedClass = ClassName.get(REGISTRY_PACKAGE, "BuiltinRegistries");

        final TypeSpec.Builder constants = TypeSpec.interfaceBuilder(generatedClass)
                .addModifiers(Modifier.PUBLIC)
                .addAnnotation(ApiStatus.NonExtendable.class)
                .addAnnotation(codegen.suppressUnused())
                .addJavadoc("Contains keys for all built-in Minecraft registries.\n<br>\nCode autogenerated, do not edit!");

        Stream.concat(
                        staticRegistries.stream().map(RegistrySpec::of),
                        dynamicRegistries.stream().map(RegistrySpec::of)
                )
                .sorted(Comparator.comparing(RegistrySpec::key))
                .forEach(spec -> {
                    final ClassName valueClass = ClassName.get(spec.packageName(), spec.typeName());
                    final ParameterizedTypeName typedRegistry = ParameterizedTypeName.get(registryClass, valueClass);
                    final ParameterizedTypeName typedRegistryKey = ParameterizedTypeName.get(registryKeyClass, typedRegistry);
                    final String constantName = codegen.constantName(spec.constantName());
                    constants.addField(FieldSpec.builder(typedRegistryKey, constantName)
                            .addModifiers(Modifier.PUBLIC, Modifier.STATIC, Modifier.FINAL)
                            .initializer("$T.unsafeOf($S)", registryKeyClass, codegen.namespaceShort(spec.key()))
                            .addJavadoc("The registry key for {@link $T}.\n", valueClass)
                            .build());
                    final String compatibilityName = codegen.constantName(spec.key());
                    if (!constantName.equals(compatibilityName)) {
                        constants.addField(FieldSpec.builder(typedRegistryKey, compatibilityName)
                                .addModifiers(Modifier.PUBLIC, Modifier.STATIC, Modifier.FINAL)
                                .addAnnotation(AnnotationSpec.builder(Deprecated.class)
                                        .addMember("forRemoval", "$L", true)
                                        .build())
                                .initializer("$L", constantName)
                                .addJavadoc("Compatibility alias for {@link #$L}.\n\n", constantName)
                                .addJavadoc("@deprecated use {@link #$L}\n", constantName)
                                .build());
                    }
                });

        addRegistry(constants, registryKeyClass, registryClass,
                ClassName.get("net.minestom.server.particle", "Particle"), "PARTICLE_TYPE", "particle_type");

        final ClassName gameRuleClass = ClassName.get("net.minestom.server.instance.gamerule", "GameRule");
        final ParameterizedTypeName gameRuleType = ParameterizedTypeName.get(gameRuleClass,
                WildcardTypeName.subtypeOf(Object.class));
        final ParameterizedTypeName gameRuleRegistry = ParameterizedTypeName.get(registryClass, gameRuleType);
        constants.addField(FieldSpec.builder(ParameterizedTypeName.get(registryKeyClass, gameRuleRegistry), "GAME_RULE")
                .addModifiers(Modifier.PUBLIC, Modifier.STATIC, Modifier.FINAL)
                .initializer("$T.unsafeOf($S)", registryKeyClass, "game_rule")
                .addJavadoc("The registry key for {@link $T}.\n", gameRuleClass)
                .build());

        codegen.write(codegen.javaFile(generatedClass.packageName(), constants.build()));
    }

    private record RegistrySpec(String key, String packageName, String typeName) {
        private String constantName() {
            return key.substring(key.lastIndexOf('/') + 1);
        }

        private static RegistrySpec of(Generators.StaticRegistrySpec spec) {
            return new RegistrySpec(spec.key(), spec.packageName(), spec.registryTypeName());
        }

        private static RegistrySpec of(Generators.DynamicRegistrySpec spec) {
            return new RegistrySpec(spec.key(), spec.packageName(), spec.typeName());
        }
    }
}
